- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *simpleTableIdentifier = @"SimpleTableCell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:simpleTableIdentifier];
    
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:simpleTableIdentifier];
    }
    
    cell.textLabel.text = [self.pages objectAtIndex:indexPath.row];
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    if (indexPath.row == 0) {
        [self performSegueWithIdentifier: @"toImages" sender: self];
    } else if (indexPath.row == 1) {
        [self performSegueWithIdentifier: @"toTimer" sender: self];
    } else if (indexPath.row == 2) {
        [self performSegueWithIdentifier: @"toButtons" sender: self];
    } else if (indexPath.row == 3) {
        [self performSegueWithIdentifier: @"toPickerSwitch" sender: self];
    }
}


